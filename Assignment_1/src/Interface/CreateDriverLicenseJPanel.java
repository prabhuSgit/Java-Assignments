    private void browseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseBtnActionPerformed
        // TODO add your handling code here:
        
        JFileChooser image = new JFileChooser();
        image.showOpenDialog(null);
        File imageFile = image.getSelectedFile();
        String fileName = imageFile.getAbsolutePath();
        browseTextField.setText(fileName);
        ImageIcon displayImage = new ImageIcon(fileName);
        displayImageBox.setIcon(displayImage);
        driverLicense.setPicture(fileName);                       
    }//GEN-LAST:event_browseBtnActionPerformed


