    private void addAirlineBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAirlineBtnActionPerformed
        // TODO add your handling code here:
        if(nameAirlineTxtField.getText().isEmpty() || codeAirlineTxtField.getText().isEmpty() || 
            desAirlineTxtField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please add all the details!");
        }else{
            
            nameAirlineTxtField.getText();
            desAirlineTxtField.getText();
            try{
            Integer.parseInt(codeAirlineTxtField.getText());
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Only numbers are allowed in Code field");
                return;
            }
             
            Airline airline = airlineDirectory.addAirline();
            airline.setName(nameAirlineTxtField.getText());
            airline.setCode(Integer.parseInt(codeAirlineTxtField.getText()));
            airline.setDescription(desAirlineTxtField.getText());
            
            JOptionPane.showMessageDialog(null, "Airline added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            
            rightJPanel.remove(this);
            CardLayout layout = (CardLayout)rightJPanel.getLayout();
            layout.previous(rightJPanel);
        }
    }//GEN-LAST:event_addAirlineBtnActionPerformed

